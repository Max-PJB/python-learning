class Solution:
    def subarraysDivByK(self, A, K):
        """
        :type A: List[int]
        :type K: int
        :rtype: int
        """
        assert len(A) > 0
        pre = {}
        res = 0
        for m in A:
            tmp = {}
            for n, k in pre.items():  # n 是key也就是前面的累加和， k 是前面出现这么多次 n 的次数
                sum_i_j = n + m
                if sum_i_j % K == 0:
                    res += k
                tmp[sum_i_j] = k
            if m % K == 0:
                res += 1
            tmp[m] = tmp.setdefault(m, 0) + 1
            pre = tmp
        return res


