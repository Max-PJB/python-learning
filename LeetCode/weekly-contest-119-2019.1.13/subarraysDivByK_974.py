"""
大神：如果两个数mod k，得到的余数相同，那么两个数一定等于n*k + 余数，那么两个数的差一定是k的整数倍，所以肯定能被k整除
https://leetcode-cn.com/problems/subarray-sums-divisible-by-k/solution/he-ke-bei-k-zheng-chu-de-zi-shu-zu-by-leetcode/
class Solution(object):
    def subarraysDivByK(self, A, K):
        P = [0]
        for x in A:
            P.append((P[-1] + x) % K)

        count = collections.Counter(P)
        return sum(v*(v-1)/2 for v in count.values())
"""


